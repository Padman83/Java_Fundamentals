public class AssignmentOperator {
    public static void main(String[] args) {

        int number1;
        int number2;

        // Assigning 15 to number 1.
        number1 = 15;
        System.out.println(number1);

        // Assigning the value of variable number2 to number1.
        number2 = number1;
        System.out.println(number2);
    }
}

