public class incrementDecrementOperator {
    public static void main(String[] args) {
        double number = 1.1;

        System.out.println(number++);
        System.out.println(number);

        System.out.println(++number);
        System.out.println(number);

        System.out.println(number--);
        System.out.println(number);

        System.out.println(--number);
        System.out.println(number);
    }
}
