public class concatenateTheString {
    public static void main(String[] args) {
        String start;
        String middle;
        String end;
        String result;

        start = "Talk is cheap. ";
        middle = "Sow me the code. ";
        end = "- Linus Torvalds. ";

        result = start + middle + end;
        System.out.println(result);
    }
}
