public class PlanetWeight {
    public static void main(String[] args) {
        System.out.println("My weight on Earth is ");
        double weight = 120;
        System.out.println(weight);

        System.out.println("My weight on Mercury is ");
        double mercury = weight  * .378;
        System.out.println(mercury);

        System.out.println("My weight on the moon is ");
        double moon = weight * .166;
        System.out.println(moon);

        System.out.println("My weight on the Jupiter is ");
        double jupiter = weight * 2.364;
        System.out.println(jupiter);

        System.out.println("My weight on the Neptune is ");
        double neptune = weight * 88.9;
        System.out.println(neptune);
    }
}
