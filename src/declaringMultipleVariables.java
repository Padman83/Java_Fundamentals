public class declaringMultipleVariables {
    public static void main(String[] args) {
        // Declaring more than one variables of the same type, use a comma to separate it.
        double orange = 1;
        double apple = 2;
        double strawberry = 8;
        double oats = 10;
        System.out.println(orange + apple + strawberry + oats);
    }
}
